/* CRIA ESQUEMA */
DROP DATABASE IF EXISTS RHCOMPLEX;

CREATE DATABASE RHCOMPLEX;

USE RHCOMPLEX;

DROP TABLE IF EXISTS CARGO_FUNC;

CREATE TABLE CARGO_FUNC (
       MATR                 varchar(10) NOT NULL,
       COD_CARGO            varchar(10) NOT NULL,
       DATA_PROMOCAO        DATE NOT NULL,
       SALARIO              numeric(8,2) NOT NULL,
       CONSTRAINT PK_CARGO_FUNC
              PRIMARY KEY (MATR, COD_CARGO, DATA_PROMOCAO)
);

DROP TABLE IF EXISTS FAIXA_SALARIAL;

CREATE TABLE FAIXA_SALARIAL (
       COD_CARGO            varchar(10) NOT NULL,
       DATA_VIG             DATE NOT NULL,
       VAL_MINIMO           numeric(8,2) NOT NULL,
       VAL_MAXIMO           numeric(8,2) NOT NULL,
       CONSTRAINT PK_FAIXA
              PRIMARY KEY (COD_CARGO, DATA_VIG)
);

DROP TABLE IF EXISTS CARGO;

CREATE TABLE CARGO (
       COD_CARGO            varchar(10) NOT NULL,
       NOME                 varchar(40) NOT NULL,
       IND_GERENCIAL        char NOT NULL,
       GRAU_ESC_MIN         int NOT NULL,
       NIVEL_HAY            int NOT NULL,
       CONSTRAINT PK_CARGO
              PRIMARY KEY (COD_CARGO)
);

DROP TABLE IF EXISTS FUNC;

CREATE TABLE FUNC (
       MATR                 varchar(10) NOT NULL,
       NOME                 varchar(60) NOT NULL,
       LOGRADOURO           varchar(60) NOT NULL,
       NUMERO               varchar(10) NOT NULL,
       COMPLTO              varchar(20) NOT NULL,
       BAIRRO               varchar(30) NOT NULL,
       CEP                  varchar(10) NOT NULL,
       MUNICIPIO            varchar(30) NOT NULL,
       DATA_ADMISSAO        DATE NOT NULL,
       DATA_NASC            DATE NOT NULL,
       SEXO                 char NOT NULL,
       GRAU_ESC             int NOT NULL,
       COD_DEPTO            varchar(10) NOT NULL,
       NUM_DEPEND           int NOT NULL,
       CONSTRAINT PK_FUNC
              PRIMARY KEY (MATR)
);

DROP TABLE IF EXISTS DEPTO;

CREATE TABLE DEPTO (
       COD_DEPTO            varchar(10) NOT NULL,
       NOME                 varchar(40) NOT NULL,
       COD_DEPTO_SUP        varchar(10) NULL,
       MATR_RESP            varchar(10) NULL,
       CONSTRAINT PK_DEPTO
              PRIMARY KEY (COD_DEPTO)
);

DROP TABLE IF EXISTS ESCOLARIDADE;

CREATE TABLE ESCOLARIDADE (
       GRAU_ESC             int NOT NULL,
       DESCRICAO            varchar(40) NOT NULL,
       CONSTRAINT PK_ESCOLARIDADE
              PRIMARY KEY (GRAU_ESC)
);


ALTER TABLE CARGO_FUNC
       ADD CONSTRAINT FK01_CARGO_CARGO_FUNC
              FOREIGN KEY (COD_CARGO)
                             REFERENCES CARGO  (COD_CARGO);


ALTER TABLE CARGO_FUNC
       ADD CONSTRAINT FK02_FUNC_CARGO_FUNC
              FOREIGN KEY (MATR)
                             REFERENCES FUNC  (MATR)
                             ON DELETE CASCADE;


ALTER TABLE FAIXA_SALARIAL
       ADD CONSTRAINT FK02_CARGO_FAIXA_SALARIAL
              FOREIGN KEY (COD_CARGO)
                             REFERENCES CARGO  (COD_CARGO)
                             ON DELETE CASCADE;


ALTER TABLE CARGO
       ADD CONSTRAINT FK02_ESCOLARIDADE_CARGO
              FOREIGN KEY (GRAU_ESC_MIN)
                             REFERENCES ESCOLARIDADE  (GRAU_ESC)
                             ON DELETE NO ACTION;


ALTER TABLE DEPTO
       ADD CONSTRAINT FK02_DEPTO_DEPTO
              FOREIGN KEY (COD_DEPTO_SUP)
                             REFERENCES DEPTO  (COD_DEPTO)
                             ON DELETE NO ACTION;


ALTER TABLE DEPTO
       ADD CONSTRAINT FK03_FUNC_DEPTO
              FOREIGN KEY (MATR_RESP)
                             REFERENCES FUNC  (MATR)
                             ON DELETE SET NULL;


ALTER TABLE FUNC
       ADD CONSTRAINT FK01_DEPTO_FUNC
              FOREIGN KEY (COD_DEPTO)
                             REFERENCES DEPTO  (COD_DEPTO)
                             ON DELETE NO ACTION;


ALTER TABLE FUNC
       ADD CONSTRAINT FK01_ESCOLARIDADE_FUNC
              FOREIGN KEY (GRAU_ESC)
                             REFERENCES ESCOLARIDADE  (GRAU_ESC)
                             ON DELETE NO ACTION;


